public class ImageSharpness
  extends AbstractTransformer {

  /** for serialization. */
  private static final long serialVersionUID = 7466006970025235243L;

  /** the image reader to use. */
  protected AbstractImageSharpness m_Algorithm;
  
  /**
   * Returns a string describing the object.
   *
   * @return 			a description suitable for displaying in the gui
   */
  @Override
  public String globalInfo() {
    return "Determines whether images are in focus ('sharp') using the specified algorithm.";
  }

  /**
   * Adds options to the internal list of options.
   */
  @Override
  public void defineOptions() {
    super.defineOptions();

    m_OptionManager.add(
	"algorithm", "algorithm",
	new Dummy());
  }

  /**
   * Sets the image sharpness algorithm to use.
   *
   * @param value 	the algorithm
   */
  public void setAlgorithm(AbstractImageSharpness value) {
    m_Algorithm = value;
    reset();
  }

  /**
   * Returns the image sharpness algorithm in use.
   *
   * @return 		the algorithm
   */
  public AbstractImageSharpness getAlgorithm() {
    return m_Algorithm;
  }

  /**
   * Returns the tip text for this property.
   *
   * @return 		tip text for this property suitable for
   * 			displaying in the GUI or for listing the options.
   */
  public String algorithmTipText() {
    return "The algorithm to use to determine whether image is in focus.";
  }

  /**
   * Returns the class that the consumer accepts.
   *
   * @return		the Class of objects that can be processed
   */
  public Class[] accepts() {
    return new Class[]{AbstractImageContainer.class};
  }

  /**
   * Returns the class of objects that it generates.
   *
   * @return		<!-- flow-generates-start -->java.lang.Boolean.class<!-- flow-generates-end -->
   */
  public Class[] generates() {
    return new Class[]{Boolean.class};
  }

  /**
   * Returns a quick info about the actor, which will be displayed in the GUI.
   *
   * @return		null if no info available, otherwise short string
   */
  @Override
  public String getQuickInfo() {
    return QuickInfoHelper.toString(this, "algorithm", m_Algorithm);
  }

  /**
   * Executes the flow item.
   *
   * @return		null if everything is fine, otherwise error message
   */
  @Override
  protected String doExecute() {
    String			result;
    AbstractImageContainer cont;
    boolean                     sharp;

    result = null;

    cont  = (AbstractImageContainer) m_InputToken.getPayload();
    sharp = m_Algorithm.isSharp(cont);
    m_OutputToken = new Token(sharp);

    return result;
  }
}
